let miLista = [24, 36, 'jorge', 'pedro', 'casa'];

console.log(miLista.includes('jorge')); // true
console.log(miLista.includes('Jorge')); // false pues es case sensitive (distingue mayúsculas de minúsculas)
console.log(miLista.includes('jorge', 1)); // true pues se encuentra en la posición 2
console.log(miLista.includes('jorge', 3)); // false pues se encuentra en la posición 2
console.log(miLista.includes('jorge', -1)); // false pues se encuentra en la posición -3
console.log(miLista.includes('jorge', -3)); // true pues se encuentra en la posición -3